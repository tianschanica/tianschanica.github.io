function getTimelineRows(cardHeight) {
  const h = canvasH();
  const tl = tlY();
  const pad = 8;
  const gap = 8;
  const upper = clamp(tl - cardHeight - gap, pad, h - cardHeight - pad);
  const lower = clamp(tl + gap, pad, h - cardHeight - pad);
  return [upper, lower];
}

function getPreferredRow(exp) {
  return ['a', 'b', 'c'].includes(exp.lane) ? 0 : 1;
}

function assignTimelineRows(cardWidth) {
  const rowRight = [Number.NEGATIVE_INFINITY, Number.NEGATIVE_INFINITY];
  const rows = new Map();
  const sorted = [...EXPEDITIONS].sort((a, b) => expToX(a) - expToX(b));

  sorted.forEach(exp => {
    const x = expToX(exp);
    const preferred = getPreferredRow(exp);
    const candidates = [preferred, preferred === 0 ? 1 : 0];
    const row = candidates.find(item => x - cardWidth / 2 >= rowRight[item] + CARD_COLLISION_GAP)
      ?? (rowRight[0] <= rowRight[1] ? 0 : 1);

    rows.set(exp.id, row);
    rowRight[row] = x + cardWidth / 2;
  });

  return rows;
}

