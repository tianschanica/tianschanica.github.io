function cssPxVar(name, fallback) {
  const raw = getComputedStyle(document.documentElement).getPropertyValue(name).trim();
  const parsed = Number.parseFloat(raw);
  return Number.isFinite(parsed) ? parsed : fallback;
}

function canvasH() {
  return dom.scrollWrap?.clientHeight || window.innerHeight - cssPxVar('--header-h', 64) - cssPxVar('--strip-h', 58);
}

function tlY() {
  return canvasH() * 0.5;
}

function yearToX(y) {
  const sameYear = EXPEDITIONS.filter(exp => exp.year === y);
  if (!sameYear.length) return TIMELINE_PAD;
  const total = sameYear.reduce((sum, exp) => sum + expToX(exp), 0);
  return total / sameYear.length;
}

function expToX(exp) {
  return exp.timelineX ?? TIMELINE_PAD;
}

function clamp(num, min, max) {
  return Math.min(max, Math.max(min, num));
}

function getFocusRatio() {
  const raw = getComputedStyle(document.documentElement).getPropertyValue('--focus-pct').trim();
  if (raw.endsWith('%')) {
    const percent = Number.parseFloat(raw);
    if (!Number.isNaN(percent)) return percent / 100;
  }
  return FOCUS_RATIO;
}

function focusOffset() {
  return dom.scrollWrap.clientWidth * getFocusRatio();
}

function focusX(scrollX = dom.scrollWrap.scrollLeft) {
  return scrollX + focusOffset();
}

function findNearestExpeditionByX(x) {
  let bestExp = null;
  let bestDist = Infinity;

  EXPEDITIONS.forEach(exp => {
    const dist = Math.abs(expToX(exp) - x);
    if (dist < bestDist) {
      bestDist = dist;
      bestExp = exp;
    }
  });

  return bestExp;
}

